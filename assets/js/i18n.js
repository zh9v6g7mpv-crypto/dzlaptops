const SUPPORTED_LANGS = ['en', 'fr', 'ar'];

function applyLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) lang = 'en';
  const dict = I18N[lang];
  const html = document.documentElement;
  html.setAttribute('lang', lang);
  html.setAttribute('dir', dict.dir);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] != null) el.textContent = dict[key];
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria');
    if (dict[key] != null) el.setAttribute('aria-label', dict[key]);
  });
  const metaKey = document.body.getAttribute('data-page-meta');
  if (metaKey && dict[metaKey]) document.title = dict[metaKey];

  try { localStorage.setItem('dz-lang', lang); } catch (e) {}

  const sel = document.getElementById('langSelect');
  if (sel) sel.value = lang;
}

function initLang() {
  let lang = 'en';
  try { lang = localStorage.getItem('dz-lang') || 'en'; } catch (e) {}
  applyLang(lang);
}
